package HW1;

public class Members {
    private String name;
    private int age;
    private float speed;

    public Members(String name, int age, float speed) {
        this.name = name;
        this.age = age;
        this.speed = speed;
    }

    public String getName() {
        return name;
    }

    public float getSpeed() {
        return speed;
    }
}
